# In Ruby, there are different *types* of data:
puts "Hello, world"

# Numbers
puts 7

# Perform simple math with numbers
puts 7+3

# Strings
puts "tacos"

# Combine strings together
puts "i like"+" tacos"
puts "i like"+" #{2+1} tacos"

# Variables
quantity=3+3+3
food="tacos"

# Combine strings and variables
puts "#{quantity} "+food

# String manipulation
puts "Hello".upcase
puts "Hello".reverse
puts "Hello".swapcase